function colorChoice (color) {
  switch (color) {
    case "red":
      alert("My favorite color is red.");
      break;
    case "blue":
      alert("My favorite color is blue.");
      break;
    case "green":
      alert("My favorite color is green.");
      break;
    case "yellow":
      alert("My favorite color is yellow.");
      break;
    case "orange":
      alert("My favorite color is orange.");
      break;
    default:
      alert("Error in javascript function.");
      break;
  }
}
